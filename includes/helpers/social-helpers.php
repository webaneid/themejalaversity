/**
 * Return SVG HTML for a brand/social icon (fill style).
 *
 * @param string $name  Icon name (facebook, instagram, youtube, twitter, linkedin, whatsapp, telegram, tiktok).
 * @param int    $size  Width and height in pixels (default 20).
 * @param string $class Optional CSS class(es).
 * @return string       Safe SVG HTML.
 */
function jalaversity_social_icon( string $name, int $size = 20, string $class = '' ): string {
	$paths = jalaversity_social_icon_paths();

	if ( ! isset( $paths[ $name ] ) ) {
		return '';
	}

	$cls_attr = $class ? ' class="' . esc_attr( $class ) . '"' : '';

	return sprintf(
		'<svg xmlns="http://www.w3.org/2000/svg" width="%1$d" height="%1$d" viewBox="0 0 24 24" aria-hidden="true"%2$s>%3$s</svg>',
		absint( $size ),
		$cls_attr,
		$paths[ $name ]
	);
}

/**
 * Echo SVG HTML for a brand/social icon.
 *
 * @param string $name  Icon name.
 * @param int    $size  Size in pixels.
 * @param string $class Optional CSS class.
 */
function jalaversity_social_icon_e( string $name, int $size = 20, string $class = '' ): void {
	// phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped
	echo jalaversity_social_icon( $name, $size, $class );
}

/**
 * Human-readable labels for social platforms.
 *
 * @param string $platform Slug.
 * @return string Label.
 */
function jalaversity_social_label( string $platform ): string {
	$labels = [
		'facebook'  => 'Facebook',
		'instagram' => 'Instagram',
		'youtube'   => 'YouTube',
		'twitter'   => 'X (Twitter)',
		'linkedin'  => 'LinkedIn',
		'whatsapp'  => 'WhatsApp',
		'telegram'  => 'Telegram',
		'tiktok'    => 'TikTok',
	];
	return $labels[ $platform ] ?? ucfirst( $platform );
}

/**
 * Render social media links from Settings Page.
 *
 * Reads URL options (social_facebook, social_instagram, etc.) and outputs
 * anchor links with brand icons. Skips platforms with no URL set.
 *
 * @param string $link_class CSS class(es) for each <a> element.
 * @param int    $icon_size  Icon size in pixels.
 */
function jalaversity_social_links( string $link_class = 'footer-social-link', int $icon_size = 18 ): void {
	$platforms = [ 'facebook', 'instagram', 'youtube', 'twitter', 'linkedin', 'whatsapp', 'telegram', 'tiktok' ];

	foreach ( $platforms as $platform ) {
		$url = jalaversity_get_option( 'social_' . $platform, '' );

		if ( empty( $url ) ) {
			continue;
		}

		$url   = esc_url( $url );
		$label = esc_attr( jalaversity_social_label( $platform ) );
		$icon  = jalaversity_social_icon( $platform, $icon_size );
		$class = esc_attr( $link_class );

		printf(
			'<a href="%s" class="%s" aria-label="%s" target="_blank" rel="noopener noreferrer">%s</a>',
			$url,
			$class,
			$label,
			$icon // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped
		);
	}
}
